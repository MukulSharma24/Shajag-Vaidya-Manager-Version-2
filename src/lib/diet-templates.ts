// Helper function to get current season
export function getCurrentSeason(): string {
    const month = new Date().getMonth() + 1; // 1-12

    if (month >= 2 && month <= 5) return 'SPRING';
    if (month >= 5 && month <= 7) return 'SUMMER';
    if (month >= 7 && month <= 9) return 'MONSOON';
    if (month >= 9 && month <= 11) return 'AUTUMN';
    return 'WINTER'; // Nov-Feb
}

// Helper to get diet plan template
export function getDietTemplate(constitution: string, season: string) {
    const constitutionKey = constitution.toUpperCase() as keyof typeof DIET_TEMPLATES;
    const seasonKey = season.toUpperCase() as keyof typeof DIET_TEMPLATES.VATA;

    return DIET_TEMPLATES[constitutionKey]?.[seasonKey] || DIET_TEMPLATES.TRIDOSHA.SPRING;
}